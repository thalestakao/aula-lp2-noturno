package br.alfa.aula03.metodos;

public class Calculadora {
	
	int somar(int a, int b) {
		int c;
		c = a + b;
		return c;
	}
	
	int subtrair(int a, int b) {
		int resultado;
		resultado = a - b;
		return resultado;
	}
	
	int multiplicar(int a, int b) {
		int resultado;
		resultado = a * b;
		return resultado;
	}
	
	int dividir(int a, int b) {
		int res;
		res = a / b;
		return res;
	}

}
